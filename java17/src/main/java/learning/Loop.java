package learning;

public class Loop {
    public static void main(String[] args) {

        // for loop = executes a block of code a limited amount of times

        for(int i =10; i >= 0; i--) {
            //i short for index
            System.out.println(i);
        }
        //count from 10 to 0

        System.out.println("Happy new year!");
    }
}
